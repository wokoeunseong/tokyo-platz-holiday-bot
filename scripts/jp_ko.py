def _greedy(s: str, table: dict) -> str:
    """문자열을 좌→우로 훑으며 table의 최장 키를 한국어로 치환.
    매칭된 토큰은 공백으로 구분, 미매칭 한자는 그대로 유지."""
    keys = sorted(table.keys(), key=len, reverse=True)
    out, i, n = [], 0, len(s)
    while i < n:
        for k in keys:
            if s.startswith(k, i):
                out.append(" " + table[k] + " ")
                i += len(k)
                break
        else:
            out.append(s[i])
            i += 1
    # 공백 정리
    return re.sub(r"\s+", " ", "".join(out)).strip()


def ko_place(name: str) -> str:
    """진앙명 → 한국어. 미등록 고유지명은 원문 유지."""
    if not name:
        return name
    if name in FOREIGN:
        return FOREIGN[name]
    table = {**PREF, **REGION}
    res = _greedy(name, table)
    return res or name


def ko_point(addr: str) -> str:
    """관측점 주소명 → 한국어(시·구·정만). 나머지 지명은 원문 유지."""
    if not addr:
        return addr
    return _greedy(addr, AREA)


def ko_title(title: str) -> str:
    """태풍/특보 제목 → 한국어."""
    if not title:
        return title
    t = title
    # 지역코드 （Ｒ０６）（Ｈ３０） 류 제거 + 전각 괄호 정리
    t = re.sub(r"（[Ａ-Ｚ０-９A-Z0-9]+）", "", t)
    for full, ko in TITLE_FULL.items():
        if full in t:
            t = t.replace(full, ko)
    for jp, ko in TITLE_TOKENS:
        t = t.replace(jp, ko)
    t = t.replace("（", "(").replace("）", ")").replace("・", "·")
    return re.sub(r"\s+", " ", t).strip()

